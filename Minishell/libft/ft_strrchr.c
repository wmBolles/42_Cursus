#include "minishell.h"

char	*ft_strrchr(const char *s, int c)
{
	char			*str;
	int				len;

	len = ft_strlen(s);
	str = (char *)s;
	while (len >= 0)
	{
		if (str[len] == (char)c)
			return ((char *)str + len);
		len--;
	}
	return (NULL);
}
